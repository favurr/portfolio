import { useRef } from "react";
import AnimatedHeaderSection from "../components/AnimatedHeaderSection";
import { AnimatedTextLines } from "../components/AnimatedTextLines";
import { useGSAP } from "@gsap/react";
import gsap from "gsap";
import { lightPosition } from "three/src/nodes/TSL.js";

const About = () => {
  const text = `I’m Emeka Favour Ezeamaka
  A Nigerian full-stack web developer
  I build scalable, high-performance apps.`;
  const aboutText = `I’m known as Favurr a twist from my middle name - Favour, Which started as a playful nickname grew into my online identity and personal brand and now lets me stand out while staying true to my creativity.

I’m passionate about shaping ideas into meaningful digital products. Over the years, I’ve worked on everything from experimental builds to polished production apps. Many of these projects are open source, reflecting my belief in building in public, and giving to the community that shaped me.

What drives me is the challenge of crafting solutions that are efficient, user-focused, and built to last. Whether I’m designing intuitive interfaces, or experimenting with new technologies, I approach each project with precision, and a commitment to excellence.`;
  const imgRef = useRef(null);
  useGSAP(() => {
    gsap.to("#about", {
      scale: 0.95,
      scrollTrigger: {
        trigger: "#about",
        start: "bottom 80%",
        end: "bottom 20%",
        scrub: true,
        markers: false,
      },
      ease: "power1.inOut",
    });

    gsap.set(imgRef.current, {
      clipPath: "polygon(0 100%, 100% 100%, 100% 100%, 0% 100%)",
    });
    gsap.to(imgRef.current, {
      clipPath: "polygon(0% 0%, 100% 0%, 100% 100%, 0% 100%)",
      duration: 2,
      ease: "power4.out",
      scrollTrigger: { trigger: imgRef.current },
    });
  });
  return (
    <section id="about" className="min-h-screen bg-black rounded-b-4xl">
      <AnimatedHeaderSection
        subTitle={"Code with purpose, Built to scale"}
        title={"About"}
        text={text}
        textColor={"text-white"}
        withScrollTrigger={true}
      />
      <div className="flex flex-col items-center justify-between gap-16 px-10 pb-16 text-xl font-light tracking-wide lg:flex-row md:text-2xl lg:text-3xl text-white/60">
        <img
          ref={imgRef}
          src="images/man.jpg"
          alt="man"
          className="w-md rounded-3xl"
        />
        <AnimatedTextLines text={aboutText} className={"w-full"} />
      </div>
    </section>
  );
};

export default About;
